def PhoneNumberValidation( form,phone):
    regex = "[0-9]{3}- [0-9]{3}-[0-9]{4}"
    match = re.search(regex, phone.data)
    if not match:
        raise ValidationError ('Le champs est incorrect !')
        
def URLFacebookValidation(form, facebook_link):
    regex = '^((?:https?\:\/\/|www\.)(?:facebook)(?:.com\/)(?:[-a-z0-9]+\.)*[-a-z0-9]+.*?)$'
    match = re.search(regex, facebook_link.data)
    if not match:
        raise ValidationError(
            'Le lien Facebook est incorrect !'
        )

